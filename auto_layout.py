def build_net_map(nets):
    """Build ref -> set(net_names) and net_name -> set(refs) maps."""
    ref_to_nets = {}
    net_to_refs = {}
    for net_entry in nets:
        net_name = net_entry['name']
        for ref, pin in net_entry['nodes']:
            ref_to_nets.setdefault(ref, set()).add(net_name)
            net_to_refs.setdefault(net_name, set()).add(ref)
    return ref_to_nets, net_to_refs


POWER_NETS = {'AGND', 'DGND', 'AVDD', 'DVDD', 'VBUS', 'VBAT', 'VBAT_RAW'}


def assign_passives_to_anchors(fps, nets, anchor_positions):
    """Assign each passive to nearest anchor it shares signal nets with."""
    ref_to_nets, net_to_refs = build_net_map(nets)
    anchor_refs = set(anchor_positions.keys())
    assignments = {}  # anchor_ref -> [passive_refs]
    _power_rr_counters = {}  # round-robin counters for power domain distribution

    for ref in fps:
        if ref in anchor_refs or ref.startswith(('H', 'RV')):
            continue
        if not ref.startswith(('R', 'C')):
            continue

        my_nets = ref_to_nets.get(ref, set())
        my_signal_nets = my_nets - POWER_NETS

        # Find anchor sharing the most signal nets
        best_anchor = None
        best_score = 0
        for net_name in my_signal_nets:
            for other_ref in net_to_refs.get(net_name, set()):
                if other_ref in anchor_refs and other_ref != ref:
                    score = len(my_signal_nets & ref_to_nets.get(other_ref, set()))
                    if score > best_score:
                        best_score = score
                        best_anchor = other_ref

        # Fallback: find anchor via shared power nets (for decoupling caps)
        if best_anchor is None:
            # Try to find via transitive signal connections (passive shares net with another
            # passive that connects to an anchor)
            for net_name in my_signal_nets:
                for mid_ref in net_to_refs.get(net_name, set()):
                    if mid_ref != ref and mid_ref.startswith(('R', 'C')):
                        mid_signal = ref_to_nets.get(mid_ref, set()) - POWER_NETS
                        for mn in mid_signal:
                            for ar in net_to_refs.get(mn, set()):
                                if ar in anchor_refs:
                                    best_anchor = ar
                                    break
                            if best_anchor:
                                break
                    if best_anchor:
                        break
                if best_anchor:
                    break

        # Last resort: distribute power-only caps round-robin across ICs in domain
        if best_anchor is None:
            my_power = my_nets & POWER_NETS
            if 'AVDD' in my_power or 'AGND' in my_power:
                analog_ics = [c for c in ['U1', 'U2', 'U5', 'U9', 'U10', 'U7', 'U8', 'U3']
                              if c in anchor_refs]
                if analog_ics:
                    idx = _power_rr_counters.get('analog', 0) % len(analog_ics)
                    best_anchor = analog_ics[idx]
                    _power_rr_counters['analog'] = idx + 1
            elif 'DVDD' in my_power or 'DGND' in my_power:
                digital_ics = [c for c in ['U6', 'U4', 'U1', 'U2', 'U5']
                               if c in anchor_refs]
                if digital_ics:
                    idx = _power_rr_counters.get('digital', 0) % len(digital_ics)
                    best_anchor = digital_ics[idx]
                    _power_rr_counters['digital'] = idx + 1

        if best_anchor is None:
            best_anchor = 'UNASSIGNED'
        assignments.setdefault(best_anchor, []).append(ref)

    return assignments


def place_passives_near_anchor(anchor_x, anchor_y, passive_refs, layout, x_dir=1):
    """Place passive components clustered around an anchor position."""
    n = len(passive_refs)
    # Adaptive columns: more passives = wider spread
    cols = min(5, max(2, n // 2))
    spacing_x = 4.5 * x_dir
    spacing_y = 3.5
    start_x = anchor_x + 6 * x_dir
    start_y = anchor_y - min(3, (n // cols) * 1.5)
    for i, ref in enumerate(sorted(passive_refs)):
        col = i % cols
        row = i // cols
        x = start_x + col * spacing_x
        y = start_y + row * spacing_y
        layout[ref] = (x, y, 0)


def place_passives_right_section(all_passives, layout, ox, oy, bw, bh):
    """Place right-section passives in available gaps between ICs and connectors.

    Right section absolute coords (ox=10, bw=100): x=80..110, y=10..90.
    Occupied: IC column x=84, muxes x=98, jacks x=102, output headers x=107.

    Passive strips (2.5mm x-spacing, 3mm y-spacing for 0402/0603):
      A: x=80..82     — left of IC column (y=20..82)
      B: x=89..95     — between IC column and muxes/jacks (y=20..58)
      C: x=89..94     — between ICs and muxes, lower (y=68..82)
      D: x=103..108   — right of jacks, gap areas (y=48..58, y=78..86)
    """
    slots = []

    # Strip A: left of IC column (3 cols)
    for col_x in [80, 82]:
        for row_y in range(20, 82, 3):
            slots.append((col_x, row_y))

    # Strip B: between IC column and jacks/muxes, upper region (3 cols)
    for col_x in [89, 91.5, 94]:
        for row_y in range(20, 58, 3):
            slots.append((col_x, row_y))

    # Strip C: between ICs and muxes, lower region (2 cols)
    for col_x in [89, 91.5]:
        for row_y in range(68, 82, 3):
            slots.append((col_x, row_y))

    # Strip D: right of jacks, in gaps (2 cols)
    for col_x in [103, 105.5]:
        for row_y in range(48, 58, 3):
            slots.append((col_x, row_y))
    for col_x in [103, 105.5]:
        for row_y in range(78, 86, 3):
            slots.append((col_x, row_y))

    # Filter out slots that are too close to placed components
    def is_clear(sx, sy, min_dist=2.5):
        for ref, (lx, ly, _) in layout.items():
            if abs(sx - lx) < min_dist and abs(sy - ly) < min_dist:
                return False
        return True

    free_slots = [s for s in slots if is_clear(s[0], s[1])]

    for i, ref in enumerate(sorted(all_passives)):
        if i < len(free_slots):
            layout[ref] = (free_slots[i][0], free_slots[i][1], 0)
        else:
            # Overflow: pack in a grid at bottom of right section
            overflow_i = i - len(free_slots)
            layout[ref] = (80 + (overflow_i % 8) * 2.5, 82 + (overflow_i // 8) * 3, 0)


