def find_footprint_lib(fpid_str):
    """Find the .pretty directory and footprint name for a given FPID string.
    Handles both 'lib:footprint' and plain 'footprint' formats."""
    if ":" in fpid_str:
        lib_name, fp_name = fpid_str.split(":", 1)
    else:
        lib_name = fpid_str
        fp_name = fpid_str

    # Check our explicit map — prioritize system KiCad 7 libs over cache (KiCad 9 format)
    if fp_name in FPLIB_MAP:
        pretty_dir, actual_fp = FPLIB_MAP[fp_name]
        for base in [KICAD_FP_DIR, CACHE_FP_DIR]:
            lib_path = base / pretty_dir
            if lib_path.exists() and (lib_path / f"{actual_fp}.kicad_mod").exists():
                return str(lib_path), actual_fp

    # Try to find by searching .pretty directories — system first
    for base in [KICAD_FP_DIR, CACHE_FP_DIR]:
        if not base.exists():
            continue
        for pretty in base.iterdir():
            if pretty.suffix == ".pretty":
                fp_file = pretty / f"{fp_name}.kicad_mod"
                if fp_file.exists():
                    return str(pretty), fp_name

    return None, None


def parse_netlist(net_path):
    """Parse KiCad netlist (.net) file to extract components and nets."""
    with open(net_path, 'r') as f:
        content = f.read()

    components = []
    # Extract component entries — find each (comp ...) block
    comp_start = re.compile(r'\(comp \(ref "([^"]+)"\)')
    for m in comp_start.finditer(content):
        ref = m.group(1)
        # Find balanced closing paren for this comp block
        start_pos = m.start()
        depth = 0
        end_pos = start_pos
        for i in range(start_pos, len(content)):
            if content[i] == '(':
                depth += 1
            elif content[i] == ')':
                depth -= 1
                if depth == 0:
                    end_pos = i + 1
                    break
        comp_block = content[start_pos:end_pos]

        # Extract value and footprint
        val_m = re.search(r'\(value "([^"]+)"\)', comp_block)
        fp_m = re.search(r'\(footprint "([^"]+)"\)', comp_block)
        value = val_m.group(1) if val_m else ""
        footprint = fp_m.group(1) if fp_m else ""

        # Extract zen name from sheetpath (preserves .zen name= parameter)
        sheet_m = re.search(r'\(sheetpath \(names "([^"]+)"\)', comp_block)
        zen_name = ""
        if sheet_m:
            zen_name = sheet_m.group(1).split('.')[0]  # Strip component suffix

        components.append({
            'ref': ref,
            'value': value,
            'footprint': footprint,
            'zen_name': zen_name,
        })

    # Extract nets — use paren-balanced extraction for multi-node nets
    nets = []
    node_pattern = re.compile(r'\(node \(ref "([^"]+)"\) \(pin "([^"]+)"\)')
    # Find each (net ...) block by balancing parentheses
    net_start = re.compile(r'\(net \(code "\d+"\) \(name "([^"]+)"\)')
    for m in net_start.finditer(content):
        net_name = m.group(1)
        # Find balanced closing paren from start of (net ...)
        start_pos = m.start()
        depth = 0
        end_pos = start_pos
        for i in range(start_pos, len(content)):
            if content[i] == '(':
                depth += 1
            elif content[i] == ')':
                depth -= 1
                if depth == 0:
                    end_pos = i + 1
                    break
        net_block = content[start_pos:end_pos]
        nodes = [(n.group(1), n.group(2)) for n in node_pattern.finditer(net_block)]
        nets.append({'name': net_name, 'nodes': nodes})

    return components, nets


def create_board(board_name, components, nets, board_config):
    """Create a KiCad PCB with placed components."""
    board = pcbnew.BOARD()

    # Set title
    title_block = board.GetTitleBlock()
    title_block.SetTitle(board_name)

    # Set design rules
    ds = board.GetDesignSettings()
    ds.SetCopperLayerCount(board_config.get('layers', 2))
    ds.m_TrackMinWidth = pcbnew.FromMM(0.2)
    ds.m_ViasMinSize = pcbnew.FromMM(0.6)
    ds.m_ViasMinDrill = pcbnew.FromMM(0.3)
    ds.m_CopperEdgeClearance = pcbnew.FromMM(0.25)

    # Set default track width and clearance
    ds.m_MinClearance = pcbnew.FromMM(0.2)

    # Load and place footprints
    placed_fps = {}
    for comp in components:
        ref = comp['ref']
        fpid = comp['footprint']

        lib_path, fp_name = find_footprint_lib(fpid)
        if lib_path is None:
            print(f"  WARNING: Could not find footprint for {ref}: {fpid}")
            continue

        try:
            fp = pcbnew.FootprintLoad(lib_path, fp_name)
        except Exception as e:
            print(f"  WARNING: Failed to load {fp_name} from {lib_path}: {e}")
            continue

        fp.SetReference(ref)
        fp.SetValue(comp['value'])
        board.Add(fp)
        placed_fps[ref] = fp

    # Create nets and assign to pads
    netinfo = board.GetNetInfo()
    for net_entry in nets:
        net_name = net_entry['name']
        net_item = pcbnew.NETINFO_ITEM(board, net_name)
        board.Add(net_item)

    # Refresh net info after adding all nets
    netinfo = board.GetNetInfo()
    nets_by_name = netinfo.NetsByName()

    for net_entry in nets:
        net_name = net_entry['name']
        if not nets_by_name.has_key(net_name):
            continue
        net_obj = nets_by_name[net_name]
        for ref, pin in net_entry['nodes']:
            if ref in placed_fps:
                fp = placed_fps[ref]
                for pad in fp.Pads():
                    if pad.GetNumber() == pin:
                        pad.SetNet(net_obj)
                        break

    return board, placed_fps


def mm_pos(x, y):
    """Create a VECTOR2I position from mm coordinates."""
    return pcbnew.VECTOR2I(pcbnew.FromMM(x), pcbnew.FromMM(y))


def place_components_grid(board, placed_fps, layout_spec):
    """Place components according to layout specification."""
    for ref, (x, y, rot) in layout_spec.items():
        if ref in placed_fps:
            fp = placed_fps[ref]
            fp.SetPosition(mm_pos(x, y))
            fp.SetOrientationDegrees(rot)


def add_board_outline(board, x, y, w, h, corner_radius=0):
    """Add rectangular board outline on Edge.Cuts layer."""
    edge_layer = board.GetLayerID("Edge.Cuts")

    # Simple rectangle outline (corner_radius ignored for API compatibility)
    for (x1, y1, x2, y2) in [
        (x, y, x + w, y),
        (x + w, y, x + w, y + h),
        (x + w, y + h, x, y + h),
        (x, y + h, x, y),
    ]:
        line = pcbnew.PCB_SHAPE(board)
        line.SetShape(pcbnew.SHAPE_T_SEGMENT)
        line.SetStart(mm_pos(x1, y1))
        line.SetEnd(mm_pos(x2, y2))
        line.SetLayer(edge_layer)
        line.SetWidth(pcbnew.FromMM(0.1))
        board.Add(line)


def add_silkscreen_labels(board, components, placed_fps, layout):
    """Add descriptive silkscreen text labels near labeled components.

    Maps zen names (from .zen name= parameter, preserved in netlist sheetpath)
    to human-readable silkscreen labels. Small passives are skipped.
    """
    silk_layer = board.GetLayerID("F.Silkscreen")

    # Zen name -> silkscreen label mapping
    # Defined here but derived from .zen name= parameters
    ZEN_LABELS = {
        # MAIN board connectors
        'J_USB': 'USB',
        'J_BAT': 'BATTERY',
        'J_AFE': 'PREAMP CABLE',
        'J_HP': 'HEADPHONE',
        'J_LINE': 'LINE OUT',
        'J_OUT_L': 'MAIN OUT L',
        'J_OUT_R': 'MAIN OUT R',
        'J_FS1': 'FOOTSWITCH 1',
        'J_FS2': 'FOOTSWITCH 2',
        # MAIN board slave sockets
        'J_LEFT_A': 'SLAVE A L',
        'J_RIGHT_A': 'SLAVE A R',
        'J_LEFT_B': 'SLAVE B L',
        'J_RIGHT_B': 'SLAVE B R',
        'J_LEFT_C': 'SLAVE C L',
        'J_RIGHT_C': 'SLAVE C R',
        'J_LEFT_D': 'SLAVE D L',
        'J_RIGHT_D': 'SLAVE D R',
        # MAIN board master ESP32 (DevKit creates J_LEFT and J_RIGHT sub-components)
        'DEVKIT_MASTER': 'MASTER ESP32',
        # MAIN board volume pots
        'RV_HPL': 'HP VOL L',
        'RV_HPR': 'HP VOL R',
        # MAIN board ICs
        'U_CHG': 'CHARGER',
        'U_AVDD': 'AVDD LDO',
        'U_DVDD': 'DVDD LDO',
        'U_ADC1': 'ADC CH1+2',
        'U_ADC2': 'ADC CH3+4',
        'U_ADC3': 'ADC CH5+6 / DAC',
        'U_ADC4': 'ADC CH7+8',
        'U_SUM': 'SUMMING AMP',
        'U_OUTBUF': 'OUT BUFFER',
        'U_MUX1': 'MUX 1',
        'U_MUX2': 'MUX 2',
        # Preamp board connectors
        'J_OUT': 'TO MAIN',
        'J_PWR': 'POWER IN',
        'U_VGND': 'VGND',
    }
    # Preamp pickup inputs and direct outputs (generated in loop)
    for i in range(1, 9):
        ZEN_LABELS[f'J_IN{i}'] = f'PICKUP {i}'
        ZEN_LABELS[f'J_DOUT{i}'] = f'DIR OUT {i}'
        ZEN_LABELS[f'RV_F{i}'] = f'GAIN {i}'

    count = 0
    for comp in components:
        ref = comp['ref']
        zen_name = comp.get('zen_name', '')

        if ref not in placed_fps or ref not in layout:
            continue

        # Look up label from zen name
        text_str = ZEN_LABELS.get(zen_name, '')
        if not text_str:
            continue

        x, y, rot = layout[ref]

        # Position and rotation depend on zen_name category
        if zen_name.startswith('J_DOUT'):
            # Direct outputs: label to the right of pins
            label_x, label_y = x + 3, y
            label_rot = 0
            h_justify = pcbnew.GR_TEXT_H_ALIGN_LEFT
        elif zen_name.startswith('J_IN'):
            # Pickup inputs: label above, rotated 90° CCW
            label_x, label_y = x, y - 6
            label_rot = 90
            h_justify = pcbnew.GR_TEXT_H_ALIGN_CENTER
        elif zen_name.startswith('RV_F'):
            # Gain trimpots: label to the left
            label_x, label_y = x - 5, y
            label_rot = 0
            h_justify = pcbnew.GR_TEXT_H_ALIGN_RIGHT
        else:
            # Default: below the component
            label_x, label_y = x, y + 3
            label_rot = 0
            h_justify = pcbnew.GR_TEXT_H_ALIGN_CENTER

        text = pcbnew.PCB_TEXT(board)
        text.SetText(text_str)
        text.SetLayer(silk_layer)
        text.SetPosition(mm_pos(label_x, label_y))
        text.SetTextSize(pcbnew.VECTOR2I(pcbnew.FromMM(1.0), pcbnew.FromMM(1.0)))
        text.SetTextThickness(pcbnew.FromMM(0.15))
        text.SetHorizJustify(h_justify)
        if label_rot != 0:
            text.SetTextAngleDegrees(label_rot)
        board.Add(text)
        count += 1

    print(f"  Added {count} silkscreen labels")


def add_ground_pour(board, net_name, layer_name, x, y, w, h):
    """Add a copper pour (zone) for ground or power plane."""
    netinfo = board.GetNetInfo()
    nets_by_name = netinfo.NetsByName()
    if not nets_by_name.has_key(net_name):
        print(f"  WARNING: Net '{net_name}' not found for copper pour")
        return

    net = nets_by_name[net_name]
    layer_id = board.GetLayerID(layer_name)

    zone = pcbnew.ZONE(board)
    zone.SetNet(net)
    zone.SetLayer(layer_id)
    zone.SetIsRuleArea(False)
    zone.SetDoNotAllowTracks(False)
    zone.SetDoNotAllowVias(False)
    zone.SetDoNotAllowPads(False)
    zone.SetDoNotAllowCopperPour(False)

    # Create outline
    outline = zone.Outline()
    outline.NewOutline()
    outline.Append(pcbnew.FromMM(x), pcbnew.FromMM(y))
    outline.Append(pcbnew.FromMM(x + w), pcbnew.FromMM(y))
    outline.Append(pcbnew.FromMM(x + w), pcbnew.FromMM(y + h))
    outline.Append(pcbnew.FromMM(x), pcbnew.FromMM(y + h))

    # Set zone properties
    zone.SetMinThickness(pcbnew.FromMM(0.25))
    zone.SetThermalReliefGap(pcbnew.FromMM(0.5))
    zone.SetThermalReliefSpokeWidth(pcbnew.FromMM(0.5))

    board.Add(zone)


def hide_fab_layers(pro_path):
    """Hide F.Fab and B.Fab layers in the .kicad_pro project file."""
    with open(pro_path, 'r') as f:
        proj = json.load(f)

    # KiCad layer IDs: F.Fab=35, B.Fab=33
    # visible_layers is a hex bitmask string; all layers on = 0xffffffffffffffff
    all_on = (1 << 64) - 1
    fab_front = 1 << 35
    fab_back = 1 << 33
    mask = all_on & ~fab_front & ~fab_back
    # KiCad format: "0x00000000_HHHHHHHH_HHHHHHHH_HHHHHHHH" (128-bit, underscore-separated)
    hex_str = f"0x00000000_00000000_{(mask >> 32) & 0xffffffff:08x}_{mask & 0xffffffff:08x}"

    proj.setdefault('board', {})
    proj['board']['visible_layers'] = hex_str

    with open(pro_path, 'w') as f:
        json.dump(proj, f, indent=2)
    print(f"  Hidden F.Fab/B.Fab in {pro_path}")


