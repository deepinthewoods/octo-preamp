def build_net_map(nets):
    """Build ref -> set(net_names) and net_name -> set(refs) maps."""
    ref_to_nets = {}
    net_to_refs = {}
    for net_entry in nets:
        net_name = net_entry['name']
        for ref, pin in net_entry['nodes']:
            ref_to_nets.setdefault(ref, set()).add(net_name)
            net_to_refs.setdefault(net_name, set()).add(ref)
    return ref_to_nets, net_to_refs


POWER_NETS = {'AGND', 'DGND', 'AVDD', 'DVDD', 'VBUS', 'VBAT', 'VBAT_RAW', 'GND', 'VGND'}


def assign_passives_to_anchors(fps, nets, anchor_positions):
    """Assign each passive to nearest anchor it shares signal nets with."""
    ref_to_nets, net_to_refs = build_net_map(nets)
    anchor_refs = set(anchor_positions.keys())
    assignments = {}  # anchor_ref -> [passive_refs]
    _power_rr_counters = {}  # round-robin counters for power domain distribution

    for ref in fps:
        if ref in anchor_refs or ref.startswith(('H', 'RV')):
            continue
        if not ref.startswith(('R', 'C')):
            continue

        my_nets = ref_to_nets.get(ref, set())
        my_signal_nets = my_nets - POWER_NETS

        # Find anchor sharing the most signal nets
        best_anchor = None
        best_score = 0
        for net_name in my_signal_nets:
            for other_ref in net_to_refs.get(net_name, set()):
                if other_ref in anchor_refs and other_ref != ref:
                    score = len(my_signal_nets & ref_to_nets.get(other_ref, set()))
                    if score > best_score:
                        best_score = score
                        best_anchor = other_ref

        # Fallback: find anchor via shared power nets (for decoupling caps)
        if best_anchor is None:
            # Try to find via transitive signal connections (passive shares net with another
            # passive that connects to an anchor)
            for net_name in my_signal_nets:
                for mid_ref in net_to_refs.get(net_name, set()):
                    if mid_ref != ref and mid_ref.startswith(('R', 'C')):
                        mid_signal = ref_to_nets.get(mid_ref, set()) - POWER_NETS
                        for mn in mid_signal:
                            for ar in net_to_refs.get(mn, set()):
                                if ar in anchor_refs:
                                    best_anchor = ar
                                    break
                            if best_anchor:
                                break
                    if best_anchor:
                        break
                if best_anchor:
                    break

        # Last resort: distribute power-only caps round-robin across ICs in domain
        if best_anchor is None:
            my_power = my_nets & POWER_NETS
            if 'AVDD' in my_power or 'AGND' in my_power:
                analog_ics = [c for c in ['U1', 'U2', 'U3', 'U4', 'U5', 'U10', 'U11', 'U8', 'U9']
                              if c in anchor_refs]
                if analog_ics:
                    idx = _power_rr_counters.get('analog', 0) % len(analog_ics)
                    best_anchor = analog_ics[idx]
                    _power_rr_counters['analog'] = idx + 1
            elif 'DVDD' in my_power or 'DGND' in my_power:
                digital_ics = [c for c in ['U7', 'U6', 'U1', 'U2', 'U3', 'U4']
                               if c in anchor_refs]
                if digital_ics:
                    idx = _power_rr_counters.get('digital', 0) % len(digital_ics)
                    best_anchor = digital_ics[idx]
                    _power_rr_counters['digital'] = idx + 1

        if best_anchor is None:
            best_anchor = 'UNASSIGNED'
        assignments.setdefault(best_anchor, []).append(ref)

    return assignments


def place_passives_near_anchor(anchor_x, anchor_y, passive_refs, layout, x_dir=1):
    """Place passive components clustered around an anchor position."""
    n = len(passive_refs)
    # Adaptive columns: more passives = wider spread
    cols = min(5, max(2, n // 2))
    spacing_x = 4.5 * x_dir
    spacing_y = 3.5
    start_x = anchor_x + 6 * x_dir
    start_y = anchor_y - min(3, (n // cols) * 1.5)
    for i, ref in enumerate(sorted(passive_refs)):
        col = i % cols
        row = i // cols
        x = start_x + col * spacing_x
        y = start_y + row * spacing_y
        layout[ref] = (x, y, 0)


def _footprint_aabb(ref, lx, ly, rot, fp_str=''):
    """Return absolute (xmin, xmax, ymin, ymax) AABB for a component.

    Uses actual F.CrtYd dimensions from KiCad footprint files for known parts,
    with 0.15mm manufacturing clearance margin on all sides (courtyard already
    includes its own margin beyond pads).
    Rotation is KiCad degrees (CCW positive, Y-axis pointing down).
    """
    margin = 0.15
    native = None

    # Footprint-string-based detection first (board-independent)
    if 'Bourns_3314J' in fp_str:
        # Bourns 3314J Vertical SMD: F.CrtYd (-2.5,-3.25) to (2.5,3.25)
        native = (-2.5, 2.5, -3.25, 3.25)
    elif 'Bourns_3296' in fp_str:
        # Bourns 3296W Vertical: ~5x11mm courtyard
        native = (-2.5, 2.5, -5.5, 5.5)
    elif 'PinHeader_2x' in fp_str or 'PinSocket_2x' in fp_str:
        if '2x05' in fp_str:
            native = (-2.54, 2.54, -1.27, 11.43)
        elif '2x06' in fp_str:
            native = (-2.54, 2.54, -1.27, 13.97)
        else:
            native = (-2.54, 2.54, -1.27, 11.43)
    elif 'PinHeader_1x' in fp_str or 'PinSocket_1x' in fp_str:
        if '1x20' in fp_str:
            native = (-1.27, 1.27, -1.27, 49.53)
        elif '1x08' in fp_str:
            native = (-1.27, 1.27, -1.27, 19.05)
        elif '1x03' in fp_str:
            native = (-1.27, 1.27, -1.27, 6.35)
        elif '1x02' in fp_str:
            native = (-1.27, 1.27, -1.27, 3.81)
        else:
            native = (-1.27, 1.27, -1.27, 3.81)

    # Fall back to ref-based heuristics
    if native is None:
        if ref.startswith('RV'):
            # Alps RK09K Vertical: F.CrtYd (-1.15,-4.15) to (13.25,9.15)
            native = (-1.15, 13.25, -4.15, 9.15)
        elif ref in ('J7', 'J12'):
            # CUI SJ1-3523N Horizontal: F.CrtYd (-6.25,-7.95) to (6.25,6.55)
            native = (-6.25, 6.25, -7.95, 6.55)
        elif ref in ('J3',):
            # PinHeader_2x05 at 90°: ~12.7mm long, 5mm wide (native: long in Y)
            native = (-2.54, 2.54, -1.27, 11.43)
        elif ref in ('J13', 'J14'):
            # PinHeader_1x03: 2.54mm wide, ~7.62mm long
            native = (-1.27, 1.27, -1.27, 6.35)
        elif ref in ('J5', 'J6'):
            # PinHeader_1x02: 2.54mm wide, 5.08mm long
            native = (-1.27, 1.27, -1.27, 3.81)
        elif ref.startswith('U'):
            if 'SOIC-16' in fp_str or '3.9x9.9' in fp_str:
                # SOIC-16_3.9x9.9mm: courtyard roughly ±4.2 x ±5.7 (symmetric)
                native = (-4.2, 4.2, -5.7, 5.7)
            else:
                # SOIC-8 / QFN-28: roughly ±3.5 symmetric
                native = (-3.5, 3.5, -3.5, 3.5)
        elif ref.startswith('H'):
            # Mounting hole: ~5mm diameter courtyard
            native = (-2.5, 2.5, -2.5, 2.5)
        elif ref.startswith(('FB', 'D')):
            # Ferrite / diode: ~4x3mm
            native = (-2.0, 2.0, -1.5, 1.5)
        else:
            # Passives — use footprint string to determine size
            if '0805' in fp_str:
                # 0805 courtyard: 2.7x1.75mm
                native = (-1.35, 1.35, -0.875, 0.875)
            elif '0603' in fp_str:
                # 0603 courtyard: 2.2x1.2mm
                native = (-1.1, 1.1, -0.6, 0.6)
            else:
                # 0402 courtyard: 1.4x0.8mm
                native = (-0.7, 0.7, -0.4, 0.4)

    xn_min, xn_max, yn_min, yn_max = native
    # Rotate the four corners and compute new AABB
    # KiCad Y-down, CCW positive: rx = x*cos(θ) + y*sin(θ), ry = -x*sin(θ) + y*cos(θ)
    theta = math.radians(rot)
    cos_t, sin_t = math.cos(theta), math.sin(theta)
    rxs, rys = [], []
    for x, y in [(xn_min, yn_min), (xn_max, yn_min), (xn_max, yn_max), (xn_min, yn_max)]:
        rxs.append(x * cos_t + y * sin_t + lx)
        rys.append(-x * sin_t + y * cos_t + ly)

    return (min(rxs) - margin, max(rxs) + margin, min(rys) - margin, max(rys) + margin)


def place_passives_right_section(right_assignments, layout, ox, oy, bw, bh, fps=None, x_min_offset=70):
    """Place passives near their parent anchor, avoiding overlaps.

    Uses AABB collision detection with actual footprint courtyard dimensions so
    asymmetric parts (potentiometers, horizontal jacks) are correctly handled.

    Args:
        right_assignments: dict of anchor_ref -> [passive_refs]
        layout: shared layout dict (modified in place)
        fps: dict of ref -> footprint object (used to determine passive sizes)
        x_min_offset: left boundary offset from ox (default 70 for main board right section)
    """
    # Board bounds, inset from board edge
    x_min, x_max = ox + x_min_offset, ox + bw - 3
    y_min, y_max = oy + 2, oy + bh - 2

    # Candidate grid with 2.0mm step — AABB check handles per-footprint clearance
    # so mixed 0402/0603/0805 are correctly spaced regardless of grid density
    step = 2.0
    all_candidates = []
    x = x_min
    while x <= x_max:
        y = y_min
        while y <= y_max:
            all_candidates.append((x, y))
            y += step
        x += step

    # Seed occupied list with AABBs for every already-placed component (left AND right sections)
    occupied = []  # list of (xmin, xmax, ymin, ymax)
    for ref, (lx, ly, rot) in layout.items():
        fp_str = fps[ref].GetFPIDAsString() if fps and ref in fps else ''
        occupied.append(_footprint_aabb(ref, lx, ly, rot, fp_str))

    def is_free(cx, cy, fp_str):
        """Return True if placing a passive at (cx,cy) doesn't overlap any occupied AABB."""
        cmin_x, cmax_x, cmin_y, cmax_y = _footprint_aabb('passive', cx, cy, 0, fp_str)
        for omin_x, omax_x, omin_y, omax_y in occupied:
            if cmin_x < omax_x and cmax_x > omin_x and cmin_y < omax_y and cmax_y > omin_y:
                return False
        return True

    def dist_sq(x1, y1, x2, y2):
        return (x1 - x2) ** 2 + (y1 - y2) ** 2

    # Process anchors smallest-group-first so each anchor secures a nearby slot before
    # large groups (e.g. U1 with 15 passives) fill the nearby candidates.
    ordered_anchors = sorted(
        right_assignments.keys(),
        key=lambda a: (len(right_assignments[a]), layout[a][1] if a in layout else 999)
    )

    placed = 0
    for anchor_ref in ordered_anchors:
        passive_list = right_assignments[anchor_ref]
        if anchor_ref not in layout:
            continue
        ax, ay, _ = layout[anchor_ref]

        # Sort candidates by distance to this anchor
        sorted_candidates = sorted(all_candidates, key=lambda c: dist_sq(c[0], c[1], ax, ay))

        for ref in sorted(passive_list):
            fp_str = fps[ref].GetFPIDAsString() if fps and ref in fps else ''

            # Find nearest free candidate to anchor
            for cx, cy in sorted_candidates:
                if is_free(cx, cy, fp_str):
                    layout[ref] = (cx, cy, 0)
                    occupied.append(_footprint_aabb('passive', cx, cy, 0, fp_str))
                    placed += 1
                    break
            else:
                # No free slot — overflow at right edge below board area
                col = placed % 10
                row = placed // 10
                layout[ref] = (ox + bw - 3 - col * step, y_max + step + row * step, 0)
                occupied.append(_footprint_aabb('passive', layout[ref][0], layout[ref][1], 0, fp_str))
                placed += 1
                print(f"  OVERFLOW: {ref} (anchor {anchor_ref}) — no free slot found")

    print(f"  Placed {placed} right-section passives near anchors")


